''' 3 - Crie um programa em Python que utilize um laço for para exibir os números de 1 a 10 na tela. '''

# Inicia um laço que vai de 1 até 10(range(1, 11) vai até 11, mas não inclui o número 11
for numero in range(1, 11):
    # Mostrar o número de 1 a 10 em tela
    print("Número: ", numero)

'''
for numero in range(1, 11) - cria um laço de repetição de 1 até 10.
A função range() gera os números de 1 a 10
'''