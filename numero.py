# Passo 1: pedir para o usuario inserir 2 números
numero1 = int(input("Digite o primeiro número: "))
numero2 = int(input("Digite o segundo número: "))

# Passo 2: soma dos 2 números
soma = numero1 + numero2

# Passo 3: multiplicação de 2 números
multip = numero1 * numero2

# Passo 4: Mostrar em tela o resultado da soma e multiplicação
print("A soma é: ", soma)
print("A multiplicação é: ", multip)

